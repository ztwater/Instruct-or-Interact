class BinaryDataProcessor:
    def convert_to_utf8(self):
        """
        Convert the binary string to utf-8 string.
        >>> bdp = BinaryDataProcessor("0110100001100101011011000110110001101111")
        >>> bdp.convert_to_utf8()
        'hello'
        """
        utf8_string = self.binary_string.decode('utf-8')
        return utf8_string